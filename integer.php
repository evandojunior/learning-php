<?php

function printer($var){
    echo $var.PHP_EOL;
}
// Declareting integers
$x = 10;

$y = 15;

// Arithmetic operations

// Sum
printer($x + $y);

// Subtratiction
printer($x - $y);

// Multiplication
printer($x * $y);

// Division
printer($x / $y);

// Module (Rest of Division)
printer($x % $y);



